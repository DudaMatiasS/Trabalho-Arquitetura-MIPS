public class Mnemonic {
    private String name;
    private String r1Type;

    public Mnemonic(String name, String r1Type) {
        this.name = name;
        this.r1Type = r1Type;
    }

    public String getName() {
        return name;
    }

    public String getR1Type() {
        return r1Type;
    }


}
